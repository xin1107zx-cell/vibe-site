function getTarotAnalysis(cards, testType) {
  if (!cards || cards.length === 0) return '塔罗牌信息不完整。';
  
  const positions = ['过去', '现在', '未来'];
  let analysis = '';
  
  cards.forEach((card, i) => {
    const pos = positions[i] || `第${i+1}张`;
    analysis += `**${pos}位置 —— ${card.name}（${card.nameEn}）**\n`;
    analysis += `${card.meaning || getTarotMeaning(card.nameEn, testType)}\n\n`;
  });
  
  return analysis.trim();
}

